package com.example.uventawh;

public class TransportItem {

    String refTransport;
    String descTransport;
    String refDriver;
    String descDriver;
    String descDocument;
    String date;

    public TransportItem(String refTransport, String descTransport, String refDriver, String descDriver, String descDocument, String date){

        this.refTransport = refTransport;
        this.descTransport = descTransport;
        this.refDriver = refDriver;
        this.descDriver = descDriver;
        this.descDocument = descDocument;
        this.date = date;

    }

}
